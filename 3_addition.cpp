// C++ program for adding two numbers;
#include<iostream>
using namespace std;

int main(){

    int amount_1;
    cout<<"Enter first amount : "; cin>>amount_1;

    int amount_2;
    cout<<"Enter second amount : "; cin>>amount_2;

    int sum = amount_1 + amount_2;
    cout<<"The final amount is : "<<sum<<endl;

   
    return 0;
}